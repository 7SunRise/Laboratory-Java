// Задание 4
    static void fourth_task() {
	System.out.print("Введите количество дорог: "); // строка, выводимая после выбора задания 4 и ожидающая значение количества дорог
	int road_count = scanner.nextInt(); // считываем количество дорог
	int optimal_height = Integer.MIN_VALUE, optimal_road = 0; // переменные для максимальной высоты грузовика и номера оптимальной дороги
	for (int i = 1; i <= road_count; i++) { // проходимся по всем дорогам последовательно
	    System.out.print("Введите количество туннелей для " + i + " дороги: "); // строка, выводимая после ввода количества дорог и ожидающая количество туннелей для данной дороги
	    int tunnel_count = scanner.nextInt(); // считываем количество туннелей
	    int min_tunnel = Integer.MAX_VALUE; // переменная, которая будет хранить минимальную высоту туннеля
	    System.out.println("Введите высоту каждого туннеля:"); // строка, выводимая после ввода количества туннеля для данной дороги и ожидающая значения высот туннелей
	    for (int j = 0; j < tunnel_count; j++) { // проодимся по всем туннелям
			int current_tunnel = scanner.nextInt(); // считываем высоту туннеля
			if (current_tunnel < min_tunnel) // если высота данного туннеля меньше минимальной высоты туннеля
		    	min_tunnel = current_tunnel; // меняем минимальную высоту туннеля
	    }
	    if (min_tunnel > optimal_height) { // если минимальная высота туннеля меньше высоты грузовика
			optimal_height = min_tunnel; // даем новое значение высоте грузовика равное минимальной высоте туннеля
			optimal_road = i; // присваиваем новое значение оптимальной дороге
	    }
	}
	String output = String.format("Оптимальные дорога и высота грузовика: %d %d", optimal_road, optimal_height); // записываем полный ответ в строку
	System.out.println(output); // выводим ответ
}



