// Задание 3
    static void third_task() {
	System.out.print("Введите координату x клада: "); // строка, выводимая после выбора задания 3 и ожидающее координату клада по x
	int x_goal = scanner.nextInt(); // считываем значение координаты клада по x
	System.out.print("Введите координату y клада: "); // строка, выводимая после ввода значения координаты клада по x и ожидающая значение координаты клада по y
	int y_goal = scanner.nextInt(); // считываем значение координаты клада по y
	int shortest_path = 0, x = 0, y = 0; // переменные указывающие минимальное количество указаний и наши координаты по x и по y
	boolean goal_reached = false; // переменная, показывающая находимся ли мы на месте клада
	System.out.println("Введите указания:"); // строка выводимая после ввода обоих координат клада и ожидающая указания с карты
	String direction = scanner.next(); // считываем направление
	int steps = scanner.nextInt(); // считываем количество шагов в данном ранее направлении
	while (!direction.equals("стоп")) { // продолжаем пока указания не закончились
	    if ((x == x_goal) && (y == y_goal)) // если координаты совпадают с местом клада
			goal_reached = true; // меняем значение на проитивоположное
	    if (direction.equals("север")) // если указание на север
			y += steps; // меняем координаты
	    else if (direction.equals("юг")) // если указание на юг
			y -= steps; // меняем координаты
	    else if (direction.equals("запад"))  // если указание на запад
			x -= steps; // меняем координаты
	    else if (direction.equals("восток")) // если указание на восток
			x += steps; // меняем координаты
	    if (!goal_reached) // если мы не на месте клада
			shortest_path++; // прибавляем количество выполненных указаний
	    direction = scanner.next(); // считываем следующее указание
	    if (!direction.equals("стоп")) // если указания не закончились
			steps = scanner.nextInt(); // считываем вновь количество шагов в данном ранее направлении
	}
	System.out.println("Минимальное количество указаний: " + shortest_path); // выводим ответ
}



